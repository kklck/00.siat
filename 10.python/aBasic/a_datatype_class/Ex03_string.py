""" [ 연습-1 ]
    날짜와 시간을 표현하는 문자열에서 '2020-02-22 : 12:05:12'
    '2020년 2월 22일' 출력
    '12시 5분' 출력
"""
import datetime
print(datetime.datetime.now())
now=str(datetime.datetime.now())


print(now[:4]+"년 " + str(int(now[5:7])) + "월 " + str(int(now[8:10]))+"일")
print(now[11:13]+"시 " + now[14:16]+"분")

def zero(a):
    return a[1] if a[0]=="0" else a

print(now[:4]+"년 " + zero(now[5:7]) + "월 " + zero(now[8:10])+"일")
print(zero(now[11:13])+"시 " + zero(now[14:16])+"분")


